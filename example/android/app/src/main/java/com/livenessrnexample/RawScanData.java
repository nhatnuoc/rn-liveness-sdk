package com.livenessrnexample;

public class RawScanData {
    private String dg1;
    private String dg2;
    private String dg13;
    private String sod;
    private String dg14;
    private String dg15;

    public RawScanData() {
    }

    public RawScanData(String dg1, String dg2, String dg13, String sod, String dg14, String dg15) {
        this.dg1 = dg1;
        this.dg2 = dg2;
        this.dg13 = dg13;
        this.sod = sod;
        this.dg14 = dg14;
        this.dg15 = dg15;
    }

    public String getDg1() {
        return dg1;
    }

    public void setDg1(String dg1) {
        this.dg1 = dg1;
    }

    public String getDg2() {
        return dg2;
    }

    public void setDg2(String dg2) {
        this.dg2 = dg2;
    }

    public String getDg13() {
        return dg13;
    }

    public void setDg13(String dg13) {
        this.dg13 = dg13;
    }

    public String getSod() {
        return sod;
    }

    public void setSod(String sod) {
        this.sod = sod;
    }

    public String getDg14() {
        return dg14;
    }

    public void setDg14(String dg14) {
        this.dg14 = dg14;
    }

    public String getDg15() {
        return dg15;
    }

    public void setDg15(String dg15) {
        this.dg15 = dg15;
    }
}
